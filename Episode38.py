# list -> array, akses data dengan index

data_list = ['ucup', 'udin']

print(data_list[0])

# dictionary (dict) -> associative array
# identifier -> key

data_dict = {
    'cp':'ucup',
    'ud':'udin',
    'dg':'dudung',
    'nm':900,
    'list':data_list,
}

print(data_dict['dg'])
print(data_dict['nm'])
print(data_dict['list'])



